import java.util.*;
import java.io.*;

class DataType {
    public static void main(String[] args) {
        int num = 5;
        float fnum = 9.5f;
        double dnum = 6.98d;
        char letter = 'F';
        boolean isTrue = true;
        
        System.out.println("Integer Example: " +num);
        System.out.println("Float Example: " +fnum);
        System.out.println("Double Example: " +dnum);
        System.out.println("Char Example: " +letter);
        System.out.println("Boolean Example: " +isTrue);
    }
}